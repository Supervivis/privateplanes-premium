const FORMAT_DATE = (dateStr, ignoreYear) => {
  const dateParts = dateStr.split('/');
  const month = parseInt(dateParts[0], 10) - 1; // Month is 0-indexed in JavaScript Date
  const day = parseInt(dateParts[1], 10);
  const year = dateParts[2];

  // Determine the ordinal indicator
  let ordinalIndicator;
  switch (day) {
    case 1:
    case 21:
    case 31:
      ordinalIndicator = "st";
      break;
    case 2:
    case 22:
      ordinalIndicator = "nd";
      break;
    case 3:
    case 23:
      ordinalIndicator = "rd";
      break;
    default:
      ordinalIndicator = "th";
  }

  // Format the date
  if (ignoreYear) return `${day}${ordinalIndicator} ${MONTH_PREFIXES[month]}`;
  return `${day}${ordinalIndicator} ${MONTH_PREFIXES[month]} ${year}`;
}

